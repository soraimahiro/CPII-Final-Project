const Card* getCardData(CardID id) {
    for (int i = 0; i < CARD_TOTAL_COUNT; i++) {
        if (cardsData[i].id == id) {
            return &cardsData[i];
        }
    }
    return NULL;
}

void shuffle(vector* v) {
    srand(time(NULL));
    if (!v || v->SIZE <= 1) return;

    for (uint32_t i = v->SIZE - 1; i > 0; --i) {
        uint32_t j = rand() % (i + 1);

        int32_t temp = v->array[i];
        v->array[i] = v->array[j];
        v->array[j] = temp;
    }
}

